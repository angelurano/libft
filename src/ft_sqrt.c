#include "libft.h"

int	ft_sqrt(int nb)
{
	int	i;

	if (nb <= 0)
		return (0);
	i = 1;
	while (i * i < nb && i <= 2147483647 / i)
		i++;
	if (i * i == nb)
		return (i);
	return (i - 1);
}
